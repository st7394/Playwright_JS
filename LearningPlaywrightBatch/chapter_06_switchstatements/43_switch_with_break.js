let a = 20;
switch (a) {
    case 10:
        console.log("a is 10");
        break;
    case 20:
        console.log("a is 20");
        break;
    case 30:
        console.log("a is 30");
        break;
    case 40:
        console.log("a is 40");
        break;
    default:
        console.log("a is not 10, 20 or 30");
}